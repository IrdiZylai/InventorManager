    private void UpdateBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_UpdateBtnMouseClicked

        if (ProdId.getText().isEmpty() || ProdName.getText().isEmpty() || ProdQty.getText().isEmpty() || ProdDesc.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Missing information");
        } else {
            try {
                Con = DriverManager.getConnection("jdbc:derby://localhost:1527/Inventorydb", "Irdi", "Inainaina1.");
                String UpdateQuery = "update Irdi.PRODUCTTBL set PRODNAME='" + ProdName.getText() + "' , PRODQTY=" + ProdQty.getText()
                        + ", PRODDESC='" + ProdDesc.getText() + "', PRODCAT='" + CatCb.getSelectedItem().toString() + "'"
                        + " where PRODID=" + ProdId.getText();
                Statement Add = Con.createStatement();
                Add.executeUpdate(UpdateQuery);
                JOptionPane.showMessageDialog(this, "Produkti u perditesua me sukses");
                SelectProd();
            } catch (SQLException e) {
                e.printStackTrace();
            }
        }


    }//GEN-LAST:event_UpdateBtnMouseClicked

